package com.peteralbus.domain;

public class WorldTime
{
    String date;
    int USA;
    int China;
    int Japan;
    int Germany;
    int UK;
    int France;
    int India;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getUSA() {
        return USA;
    }

    public void setUSA(int USA) {
        this.USA = USA;
    }

    public int getChina() {
        return China;
    }

    public void setChina(int china) {
        China = china;
    }

    public int getJapan() {
        return Japan;
    }

    public void setJapan(int japan) {
        Japan = japan;
    }

    public int getGermany() {
        return Germany;
    }

    public void setGermany(int germany) {
        Germany = germany;
    }

    public int getUK() {
        return UK;
    }

    public void setUK(int UK) {
        this.UK = UK;
    }

    public int getFrance() {
        return France;
    }

    public void setFrance(int france) {
        France = france;
    }

    public int getIndia() {
        return India;
    }

    public void setIndia(int india) {
        India = india;
    }
}
